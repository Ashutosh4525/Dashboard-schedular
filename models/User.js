import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
   username: {
      type: String,
      required: true,   
      unique: true,    
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },

     password: {
      type: String,
      required: true,
    },
  role: {
    type: String,
    enum: ["admin", "parent", "student","teacher"],
    default: "student",
  },
});

export default mongoose.models.User ||
  mongoose.model("User", UserSchema);
